#คำสั่ง elif (else if) ใช้เพื่อตรวจสอบเงื่อนไขเพิ่มเติม
#ถ้าเงื่อนไข if เป็นเท็จ จะตรวจสอบเงื่อนไข elif ถ้าเป็นจริงจะทำตามคำสั่งภายใน elif
#ถ้าเงื่อนไข elif เป็นเท็จ จะทำตามคำสั่งภายใน else (ถ้ามี) 



my_score =int(input("กรอกคะแนนของคุณ: "))

if my_score > 80:
    print("สอบผ่าน - ยอดเยี่ยม")

elif my_score >= 50:
    print("สอบผ่าน")

else:
    print("สอบตก")