#สัญลักษณ์เปรียบเทียบ > มากกว่า
#สัญลักษณ์เปรียบเทียบ == เท่ากับ
#สัญลักษณ์เปรียบเทียบ != ไม่เท่ากับ
#สัญลักษณ์เปรียบเทียบ >= มากกว่าหรือเท่ากับ
#สัญลักษณ์เปรียบเทียบ <= น้อยกว่าหรือเท่ากับ
#การเปรียบเทียบจะได้ผลลัพธ์เป็น boolean
#เช่น ถ้า x < y จะได้ผลลัพธ์เป็น True ถ้า x น้อยกว่า y
#ถ้า x >= y จะได้ผลลัพธ์เป็น False ถ้า x ไม่มากกว่า y   

x = 5
y = 4

equal = x==y
more = x>y
less = x<y

print(equal,more,less)