#in statement ใช้ in
#ใช้เพื่อตรวจสอบว่าสิ่งหนึ่งเป็นสมาชิกของอีกสิ่งหนึ่งหรือไม่
#เช่น ถ้า x เป็นสมาชิกของ my_list จะได้ผลลัพธ์เป็น True
#ถ้า x ไม่เป็นสมาชิกของ my_list จะได้ผลลัพธ์เป็น False  

#in ใน int
my_list = [5,6,7]

print(5 in my_list)
print("c" in my_list)

#in ใน string
my_string = "hello world"
print("h" in my_string)
print("z" in my_string)